
public class DataTypes {
	public static void main(String[] args) {
		boolean a=true;
		char b='A';
		char c='a';
		byte d =10;   //value lies between -128  to 127  , default value is zero
		byte e =-11;
		short f= 1000;  //value ranges between -32,768  to 32,767 , default value is zero
		short g=-1100;
		int h=   10000    ; //range between (-2^31) to  (2^31)-1  
		long i=  100000000L           ;   //range between -2^63  to (2^63)-1
		float j= 234.5f;    
		double k= 12.3;
	}
}
